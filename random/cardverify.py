def count_matches(reference_string, list_of_strings):
    """
    Count how many integers in each string from the list appear in the reference string.

    Args:
        reference_string (str): A space-delimited string of integers
        list_of_strings (list): A list of space-delimited strings of integers

    Returns:
        list: A list of counts, one for each string in list_of_strings
    """
    # Convert reference string to a set of integers for O(1) lookups
    reference_set = set(map(int, reference_string.split()))

    # Process each string in the list
    result = []
    for i, string in enumerate(list_of_strings):
        # Convert current string to integers
        integers = list(map(int, string.split()))
        card = integers[0]
        lits = integers[1:]
        #print(i, len(lits))
        # Count matches
        matches = sum(1 for num in lits if num in reference_set)
        if matches < card:
            lits_not_sat = [num for num in lits if num not in reference_set]
            print(f"mismatch ({matches}):", len(lits_not_sat), "-", lits_not_sat)
            print("\t", string, f'\n\t{len(lits)} - {lits} \n\t{len(integers)} - {integers}')
            result.append(False)
        else:
            result.append(True)

    return "SAT" if all(result) else "UNSAT"


def combine_and_adjust_files(source_file, target_file, output_file):
    source_shift_values = []
    target_shift_values = []
    combined_lines = []
    found_p_in_source = False
    found_p_in_target = False

    # Scan target file for its shift values
    with open(target_file, "r") as file:
        for line in file:
            words = line.strip().split()
            if words and words[0] == "p":
                prefix = [words[0]] + [w for w in words[1:] if not w.isdigit()]
                target_shift_values = [int(w) for w in words[1:] if w.isdigit()]
                found_p_in_target = True
                break
        if not found_p_in_target:
            raise ValueError("No 'p' line found in target file")

        # Scan source file, extract its shift value, and adjust 'p' line
        with open(source_file, "r") as file2:
            for line in file2:
                words = line.strip().split()
                if words and words[0] == "p":
                    source_prefix = [words[0]] + [w for w in words[1:] if not w.isdigit()]
                    source_shift_values = [int(w) for w in words[1:] if w.isdigit()]
                    adjusted_p_line = source_prefix + [str(s + t) for s, t in zip(source_shift_values, target_shift_values)]
                    combined_lines.append(" ".join(adjusted_p_line))
                    found_p_in_source = True
                else:
                    combined_lines.append(line.strip())
            if not found_p_in_source:
                raise ValueError("No 'p' line found in source file")

        for line in file:
            words = line.strip().split()
            if words:
                adjusted_words = []
                first_int_found = False
                for word in words:
                    if word.lstrip("-").isdigit():
                        num = int(word)
                        if not first_int_found:
                            adjusted_words.append(str(num))  # Keep first integer unchanged
                            first_int_found = True
                        elif num == 0:
                            adjusted_words.append("0")  # Ignore zeros
                        elif num < 0:
                            adjusted_words.append(str(num - source_shift_values[0]))
                        else:
                            adjusted_words.append(str(num + source_shift_values[0]))
                    else:
                        adjusted_words.append(word)
                combined_lines.append(" ".join(adjusted_words))

    # Write the combined output to a new file
    with open(output_file, "w") as file:
        file.write("\n".join(combined_lines) + "\n")


